#include "push_swap.h"

int	ft_sb(int *sb)
{
	int tmp;
	
	if (!sb || sb[1] == '\0')
		return (0);
	tmp = sb[0];
	sb[0] = sb[1];
	sb[1] = tmp;
	return (1);
}
